
package revisandopoo;

public class Encapsulamento {
   /*
    PILARES DA POO 
    Sigla EHP - Encapsulamento, Herança, Polimorfismo
    Abstração pode ser considerada um pilar. Na maioria das bibliografias, está atrelada ao encapsulamento
    
    Encapsulamento - Cápsula
    Cria padrão e garante mais segurança aos usuários.
    Padroniza objetos
    Não deixa o programador fazer besteiras, usam interfaces e módulos padrão.
    
    Encapsular - Ocultar partes independentes da implementação, permitindo construir partes invisíveis ao mundo exterior. 
    Sabe que o resultado será sempre o mesmo.
    Mensagem - Troca de interação com a cápsula e o mundo externo. 
    Pessoa e programado interagem com o sistema/capsula, e o objeto retorna a resposta.
    Interface - Lista de serviços fornecidos por um componente. É o contato com o mundo exterior, 
    que define o que pode ser feito com um objeto dessa classe.
    
    Vantagens de Encapsular 
    1- Tornar mudanças invisíveis. Mantendo a interface, pode-se modificar o software.
    2- Facilitar a reutilização do código. O encapsulamento profundo permite o padrão.
    3- Reduzir efeitos colaterais. Possíveis danos no funcionamento do software.     
    */   
}
